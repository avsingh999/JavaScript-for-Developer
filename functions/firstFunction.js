//Simple function to add to numbers

function add(num1, num2){
    return num1+num2
}
console.log("Addition of 3 and 4 = ",add(3,4))

//function with ES6 or Javascript6 notation

const product = (num1,num2) =>{
    return num1*num2
}
console.log("product of 3 and 4 = ",product(3,4))

//ES6 shorthand when the function body has only one statement

const subtract = (num1,num2) => num1-num2

console.log("Subtration of 3 from 10 = ",subtract(10,3))


